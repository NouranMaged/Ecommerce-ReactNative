import { Dimensions } from "react-native";
const { height, width } = Dimensions.get("window");

export const actualDimensions = {
  height: height < width ? width : height,
  width: width > height ? height : width,
};
export const responsiveWidth = (width) => {
  return (Dimensions.get("window").width * width) / actualDimensions.width;
};

export const responsiveHeight = (height) => {
  return (Dimensions.get("window").height * height) / actualDimensions.height;
};
export default { responsiveWidth, responsiveHeight, actualDimensions };
